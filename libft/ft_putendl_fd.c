#include "libft.h"

void ft_putendl_fd(char *s, int fd) {
	char c;

	c = '\n';
	if(fd != -1) {
		if(s != 0) {
			while(*s)
				write(fd, s++, 1);
		}
		write(fd, &c, 1);
	}
}
